package com.alpermelkeli.composedrawing

/**
 * Enum representing available drawing tools.
 */
enum class DrawTools {
    /** Eraser tool - draws with background color */
    ERASER,
    /** Primary pencil tool - solid strokes */
    PENCIL1,
    /** Secondary pencil tool - semi-transparent strokes (highlighter) */
    PENCIL2
}
